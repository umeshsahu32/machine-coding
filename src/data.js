import React, { useState, useEffect, useRef } from "react";
import styles from "./StoryCard.module.css";

const StoryCard = ({ cardData }) => {
  // We'll show first 30 cards immediately since data is static
  const INITIAL_DISPLAY = 30;
  const CARDS_PER_BATCH = 30;

  const [visibleCount, setVisibleCount] = useState(INITIAL_DISPLAY);
  const loaderRef = useRef(null);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        const target = entries[0];
        if (target.isIntersecting) {
          // Immediately show next batch since data is already available
          setVisibleCount((prevCount) =>
            Math.min(prevCount + CARDS_PER_BATCH, cardData.length)
          );
        }
      },
      {
        root: null,
        rootMargin: "100px",
        threshold: 0.1,
      }
    );

    if (loaderRef.current) {
      observer.observe(loaderRef.current);
    }

    return () => {
      if (loaderRef.current) {
        observer.unobserve(loaderRef.current);
      }
    };
  }, [cardData.length]);

  return (
    <div className={styles.cardSection}>
      {cardData.slice(0, visibleCount).map((item, index) => (
        <div
          className={styles.card}
          key={`card-${index}`}
          style={{ background: item.gradient }}
        >
          <div className={styles.logoContainer}>
            <img src={item.image} alt="Company Logo" className={styles.logo} />
          </div>
          <div className={styles.content}>
            <span className={styles.percentage}>{item.percentage}</span>
            <span className={styles.description}>{item.description}</span>
          </div>
          <div className={styles.cardContent}>
            <div className={styles.cardDetails}>
              <div
                className={`${styles.info_content} ${styles.info_content_1}`}
              >
                <p>Challenge:</p>
                <span>{item.challenge}</span>
              </div>
              <div
                className={`${styles.info_content} ${styles.info_content_2}`}
              >
                <p>Solution:</p>
                <span>{item.solution}</span>
              </div>
              <div
                className={`${styles.info_content} ${styles.info_content_3}`}
              >
                <p>Result:</p>
                <span>{item.result}</span>
              </div>
            </div>
          </div>
        </div>
      ))}
      {visibleCount < cardData.length && (
        <div ref={loaderRef} className={styles.invisibleLoader} />
      )}
    </div>
  );
};

export default StoryCard;

